def make_walls(matrix):
    wall_rects = []
    wall_rects_left = []
    wall_rects_right = []
    wall_rects_bottom = []
    for row_idx, row in enumerate(matrix):
        for i, unit in enumerate(row):
            left = i * 15
            top = row_idx * 15
            grid_square = pygame.Rect((left, top), (GRID_UNIT, GRID_UNIT))
            row[i] = {'grid_square': grid_square, 'has_block': False}

        if i == 0 or i == 25 or row_idx == 39:
            row[i]['has_block'] = True
            border_square = pygame.Surface((GRID_UNIT, GRID_UNIT))
            border_square.fill(BLACK)
            wall_rects.append((left, top))
            if i == 0:
                wall_rects_left.append((left, top))
            if i == 25:
                wall_rects_right.append((left, top))
            if row_idx == 39:
                wall_rects_bottom.append((left, top))
    return [matrix, wall_rects, wall_rects_left, wall_rects_bottom, wall_rects_right]